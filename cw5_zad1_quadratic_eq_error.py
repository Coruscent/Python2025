#Napisz skrypt, który przyjmuje od użytkownika 3 liczby całkowite a,b,c następnie oblicza wyrażenie kwadratowe i zwraca jego pierwiastki. Pamiętaj o obsłudze wyjątków:
#- ValueError - gdy użytkownik poda coś innego niż liczbe całkowitą
#- ZeroDivisionError - gdy a = 0 (wtedy to nie jest wyrażenie kwadratowe)
